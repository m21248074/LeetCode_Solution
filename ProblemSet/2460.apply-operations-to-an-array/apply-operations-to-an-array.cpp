class Solution {
public:
    vector<int> applyOperations(vector<int>& nums) {
        int n=nums.size();
        for(int i=0;i<n-1;i++)
        {
            if(nums[i]==nums[i+1])
            {
                nums[i]*=2;
                nums[i+1]=0;
            }
        }
        stable_sort(nums.begin(),nums.end(),[](int a,int b){
            return !a<!b;
        });
        return nums;
    }
};